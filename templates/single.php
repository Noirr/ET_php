                        <h1 class="et-single-post-title">Hiszpania: Atletico zmniejszyło swoje szanse na skuteczny pościg za Barceloną</h1>
                        
                        <!-- Post content -->
                        <p class="et-single-post-content"><strong>Atletico Madryt tylko zremisowało u siebie z Girona FC 1:1. Ten remis zmniejszył szansę podopiecznych Diego Simeone na dogonienie liderującej w tabeli Barcelony. Bramkę dla gospodarzy zdobył Antoine Griezmann, wyrównał Portu.</strong><br/><br/>
                        Ten mecz Atletico musiało wygrać, aby mieć szansę na skuteczny pościg za FC Barcelona. Przewaga Blaugrany przed tym spotkaniem wynosiła dziewięć punktów. Teraz może wzrosnąć, bo piłkarze z Madrytu swój mecz tylko zremisowali.<br/><br/>
                        Wszystko zaczęło się zgodnie z oczekiwaniami. W 33. minucie do bramki rywali trafił Griezmann. Gospodarze w tej akcji "złamali pułapkę ofsajdową" przeciwników. Błyskawicznie przed bramkarzem Girony znalazł się Diego Costa, a po otrzymaniu prostopadłego podania, zagrał w tył do wbiegającego Francuza. Napastnik Atletico nie miał problemu ze strzeleniem gola. Protesty gospodarzy, którzy domagali się odgwizdania spalonego, na nic się nie zdały.<br/><br/>
                        Goście wyrównali w 73. minucie, gdy udało im się wykorzystać zamieszanie podbramkowe. Co ciekawe Girona była bliska zdobycie bramki chwilę wcześniej, ale Atletico na moment się wyratowało z opresji. Piłka błyskawicznie wróciła w pole karne, a wślizgiem do bramki faworyzowanych przeciwników posłał ją Portu czyli Cristian Portugués.<br/><br/>
                        Atletico mogło przegrać ten mecz. W 90. minucie pojedynek szybkościowy z obrońcami gospodarzy wygrał Michael Olunga Ogada i zbliżył się z piłką do bramki Jana Oblaka. Słoweński bramkarz szybko z niej wyszedł i sfaulował Kenijczyka. Sędzia miał prawo pokazać mu czerwoną kartkę, ale wyjął tylko żółtą. Wynik już się nie zmienił.<br/><br/>
                        Remis Atletico oznacza, że FC Barcelona w przypadku wygranej z Betisem Sewilla będzie miała jedenaście punktów przewagi. Girona FC nie poprawiła znacząco swojej pozycji.</p>
                        
